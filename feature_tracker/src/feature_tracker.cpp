void FeatureTracker::setMask(vector<int> &indices)
{
    if(FISHEYE)
        mask = fisheye_mask.clone();
    else
        mask = cv::Mat(ROW, COL, CV_8UC1, cv::Scalar(255));
    

    // prefer to keep features that are tracked for long time
    vector<pair<int, pair<pair<int, cv::Point2f>, int>>> cnt_pts_id;

    for (unsigned int i = 0; i < forw_pts.size(); i++)
        cnt_pts_id.push_back(make_pair(track_cnt[i], make_pair(make_pair(indices[i], forw_pts[i]), ids[i])));

    if (forw_pts.size() >= 1)
        for (int i = 0; i < forw_pts.size()-1; i++)
            if (track_cnt[i] < track_cnt[i+1])
                    ROS_ERROR("Track count not in order");

    forw_pts.clear();
    ids.clear();
    track_cnt.clear();
    indices.clear();

    for (auto &it : cnt_pts_id)
    {
        if (mask.at<uchar>(it.second.first.second) == 255)
        {
            forw_pts.push_back(it.second.first.second);
            ids.push_back(it.second.second);
            track_cnt.push_back(it.first);
            indices.push_back(it.second.first.first);
            cv::circle(mask, it.second.first.second, MIN_DIST, 0, -1);
        }
    }
}

void FeatureTracker::addPoints()
{   
    for (auto &p : n_pts)
    {
        forw_pts.push_back(p);
        ids.push_back(-1);
        track_cnt.push_back(1);
    }
}

void FeatureTracker::readImage(const cv::Mat &_img, const std_msgs::Header& header)
{
    cv::Mat img;
    TicToc t_r;
    cur_time = header.stamp.toSec();

    cv::Mat bgr_img, gray_img;
    bgr_img = _img.clone();  // Keep the original BGR image
    cv::cvtColor(bgr_img, gray_img, cv::COLOR_BGR2GRAY);  // Convert without modifying _img

    if (EQUALIZE)
    {
        cv::Ptr<cv::CLAHE> clahe = cv::createCLAHE(3.0, cv::Size(8, 8));
        TicToc t_c;
        clahe->apply(gray_img, img);
        ROS_DEBUG("CLAHE costs: %fms", t_c.toc());
    }
    else
        img = gray_img;

    if (forw_img.empty())
    {
        prev_img = cur_img = forw_img = img;
    }
    else
    {
        forw_img = img;
    }

    forw_pts.clear();
    vector<int> indices;
    int num_queries = 0;

    // Call the service
    vector<uchar> track_status;
    trackon_pkg::trackon srv = createRequest(n_pts, removed_indices, bgr_img, header);
    if (client_.call(srv)) {
        const auto forw_pts_msg = srv.response.forward_points;
        if (forw_pts_msg.points.size() > 0) {
            auto result = readResponse(forw_pts_msg);
            forw_pts = result.first;
            track_status = result.second;
            for(int i=0; i<forw_pts.size(); i++)
                indices.push_back(i);
            num_queries = forw_pts.size();
        }
    } else {
        ROS_ERROR("Failed to call trackon service.");
    }

    removed_indices.clear();
    n_pts.clear();

    if (cur_pts.size() > 0)
    {
        TicToc t_o;

        for (int i = 0; i < int(forw_pts.size()); i++)
            if (track_status[i] && !inBorder(forw_pts[i]))
                track_status[i] = 0;
        
        reduceVector(prev_pts, track_status);
        reduceVector(cur_pts, track_status);
        reduceVector(forw_pts, indices, track_status);
        reduceVector(ids, track_status);
        reduceVector(cur_un_pts, track_status);
        reduceVector(track_cnt, track_status);
        ROS_DEBUG("temporal optical flow costs: %fms", t_o.toc());
    }
    ROS_DEBUG_STREAM("Tracks: " << forw_pts.size());

    for (auto &n : track_cnt)
        n++;

    if (PUB_THIS_FRAME)
    {
        rejectWithF(indices);
        ROS_DEBUG("set mask begins");
        ROS_DEBUG_STREAM("RANSAC: " << forw_pts.size());
        TicToc t_m;
        setMask(indices);
        ROS_DEBUG_STREAM("Mask: " << forw_pts.size());
        ROS_DEBUG("set mask costs %fms", t_m.toc());

        ROS_DEBUG("detect feature begins");
        TicToc t_t;
        int n_max_cnt = MAX_CNT - static_cast<int>(forw_pts.size());
        if (n_max_cnt > 0)
        {
            if(mask.empty())
                cout << "mask is empty " << endl;
            if (mask.type() != CV_8UC1)
                cout << "mask type wrong " << endl;
            if (mask.size() != forw_img.size())
                cout << "wrong size " << endl;
            cv::goodFeaturesToTrack(forw_img, n_pts, MAX_CNT - forw_pts.size(), 0.01, MIN_DIST, mask);
        }
        ROS_DEBUG("detect feature costs: %fms", t_t.toc());

        ROS_DEBUG("add feature begins");
        TicToc t_a;
        addPoints();
        ROS_DEBUG("selectFeature costs: %fms", t_a.toc());
    }
    prev_img = cur_img;
    prev_pts = cur_pts;
    prev_un_pts = cur_un_pts;
    cur_img = forw_img;
    cur_pts = forw_pts;
    undistortedPoints();
    prev_time = cur_time;

    std::unordered_set<int> indices_set(indices.begin(), indices.end());
    for (int i=0; i<num_queries; i++) {
        if (indices_set.find(i) == indices_set.end()) {
            removed_indices.push_back(i);
        }
    }
}

void FeatureTracker::rejectWithF(vector<int> &indices)
{
    if (forw_pts.size() >= 8)
    {
        ROS_DEBUG("FM ransac begins");
        TicToc t_f;
        vector<cv::Point2f> un_cur_pts(cur_pts.size()), un_forw_pts(forw_pts.size());
        for (unsigned int i = 0; i < cur_pts.size(); i++)
        {
            Eigen::Vector3d tmp_p;
            m_camera->liftProjective(Eigen::Vector2d(cur_pts[i].x, cur_pts[i].y), tmp_p);
            tmp_p.x() = FOCAL_LENGTH * tmp_p.x() / tmp_p.z() + COL / 2.0;
            tmp_p.y() = FOCAL_LENGTH * tmp_p.y() / tmp_p.z() + ROW / 2.0;
            un_cur_pts[i] = cv::Point2f(tmp_p.x(), tmp_p.y());

            m_camera->liftProjective(Eigen::Vector2d(forw_pts[i].x, forw_pts[i].y), tmp_p);
            tmp_p.x() = FOCAL_LENGTH * tmp_p.x() / tmp_p.z() + COL / 2.0;
            tmp_p.y() = FOCAL_LENGTH * tmp_p.y() / tmp_p.z() + ROW / 2.0;
            un_forw_pts[i] = cv::Point2f(tmp_p.x(), tmp_p.y());
        }

        vector<uchar> status;
        cv::findFundamentalMat(un_cur_pts, un_forw_pts, cv::FM_RANSAC, F_THRESHOLD, 0.99, status);
        int size_a = cur_pts.size();
        reduceVector(prev_pts, status);
        reduceVector(cur_pts, status);
        reduceVector(forw_pts, indices, status);
        reduceVector(cur_un_pts, status);
        reduceVector(ids, status);
        reduceVector(track_cnt, status);
        ROS_DEBUG("FM ransac: %d -> %lu: %f", size_a, forw_pts.size(), 1.0 * forw_pts.size() / size_a);
        ROS_DEBUG("FM ransac costs: %fms", t_f.toc());
    }
}

bool FeatureTracker::updateID(unsigned int i)
{
    if (i < ids.size())
    {
        if (ids[i] == -1)
            ids[i] = n_id++;
        return true;
    }
    else
        return false;
}

